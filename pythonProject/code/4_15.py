# 打印任务说明
print("公鸡5元1只，母鸡3元1只，小鸡1元3只，百元买百鸡有以下购买方案：")

# 遍历公鸡可能的数量，最多可以买20只（因为5*20=100）
for cock in range(0, 21):
    # 遍历母鸡可能的数量，最多可以买33只（因为3*33=99）
    for hen in range(0, 34):
        # 小鸡的数量是100减去公鸡和母鸡的数量
        chick = 100 - cock - hen
        # 如果当前组合的总价等于100元，则打印这个购买方案
        if 5 * cock + 3 * hen + chick / 3 == 100:
            print(f"公鸡：{cock}\t母鸡：{hen}\t小鸡：{chick}")
